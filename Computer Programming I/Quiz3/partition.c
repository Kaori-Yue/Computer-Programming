#include <stdio.h>

int main() {
    int w, n;
    scanf("%i %i", &w, &n);

    for(int i=1; i<=3; i++) {
        for(int j=1; j<=(n*w)+n+1; j++) {
            //if (i==1 || i==3) printf("*");
            //if (i==2) printf("%i ", j);
            if (i==1 || i==3) printf("*");
            else if(i==2 && j%(n+1) == 1) printf("*");
            else printf(" ");
        }
        printf("\n");
    }
}

